        String str ="1000011";
        System.out.println(ChuckNorris.convert(str));

        String ts;
        ts =  ChuckNorris.stringToBin("C");
        assertEquals("1000011",ts);
        ts =  ChuckNorris.stringToBin("%");
        assertEquals("0100101",ts);
        ts =  ChuckNorris.stringToBin(" ");
        assertEquals("0100000",ts);
        ts =  ChuckNorris.stringToBin("CC");
